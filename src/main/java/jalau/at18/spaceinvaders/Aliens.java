package jalau.at18.spaceinvaders;

public class Aliens {
    private int posX;
    private int posY;

    public Aliens() {

    }
    public void setPosX(int newPosition) {
        posX = newPosition;
    }
    public void setPosY(int newPosition) {
        posY = newPosition;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
}
